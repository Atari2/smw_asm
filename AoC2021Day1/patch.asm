macro load_label_offset_x(at, label)
    php
    ;; load bank with A in 8 bit
    sep #$20
    lda.b #<label>>>16&$FF
    sta <at>+2

    ;; load rest of 24 bit, sum X to it
    rep #$30
    lda $04
    pha
    lda.w #<label>&$FFFF
    stx $04
    clc : adc $04
    sta <at>
    pla
    sta $04
    plp
endmacro

