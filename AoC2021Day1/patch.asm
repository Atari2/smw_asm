macro read_string_into_temp(index)
    lda !input_ptr_off
    tax
    %load_label_offset_x($00, input)
    jsr strtoint
    sta !temp_buffer+<index>
    lda $05
    clc : adc !input_ptr_off
    sta !input_ptr_off
endmacro

macro check_end_of_input()
    lda !input_ptr_off
    tax
    %load_label_offset_x($00, input)
    lda [$00]
    and #$00ff
    cmp #$00ff
    bne ?ok
    jmp .end_of_input
    ?ok
endmacro

macro sum()
    lda #$0000
    clc : adc !temp_buffer
    adc !temp_buffer+2
    adc !temp_buffer+4
    sta !sum
    cmp !previous_sum
    beq .equal
    bcc .smaller
    lda !result
    inc 
    sta !result
    .equal
    .smaller
    lda !sum
    sta !previous_sum
endmacro

run_code:
    rep #$30
    lda #$ffff
    sta !previous_sum
    lda #$0000
    sta !sum
    sta !input_ptr_off
    sta !input_ptr_off_copy
    sta !result
    jsr zero_temp_buffer

    .continue
    lda !input_ptr_off_copy
    sta !input_ptr_off
    %read_string_into_temp(0)
    lda !input_ptr_off
    sta !input_ptr_off_copy
    %check_end_of_input()
    %read_string_into_temp(2)
    %check_end_of_input()
    %read_string_into_temp(4)
    %sum()
    %check_end_of_input()
    jsr zero_temp_buffer
    jmp .continue
    .end_of_input
    sep #$30
    rtl


; 24-bit address in $00, $01, $02
; returns 16-bit length in X
strlen:
    rep #$10
    ldy #$0000
    .loop
    lda [$00],y
    beq .end_strlen
    iny
    bra .loop
    .end_strlen
    tyx
    rts


; $00, $01, $02 -> 24-bit address to str
; result in 16 bit A
; puts length of string+null terminator in $04-$05
strtoint:
    !diff = $0030
    !power_of_ten = $03
    !partial = $0C
    sep #$30
    jsr strlen
    rep #$30
    stx $05
    inc $05
    dex
    stz !partial
    ldy #$0000
    .loop
        lda [$00],y
        and #$00ff
        sec : sbc #!diff
        stx !power_of_ten
        cpx #$0000
        beq .skip_loop
        .inner_loop
            jsr mult_by_ten
            dex
        bne .inner_loop
        .skip_loop
        clc : adc !partial
        sta !partial
        iny
        dec !power_of_ten
        ldx !power_of_ten
    bpl .loop
    rts

; input in 16 bit A
; output in 16 bit A
; does 2*a + 8*a
mult_by_ten:
    asl                     ;; *2
    sta $0e
    asl : asl               ;; *8
    adc $0e                 ;; +
    rts


